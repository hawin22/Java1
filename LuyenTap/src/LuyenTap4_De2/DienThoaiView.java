    private void btnThemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThemMouseClicked
        // TODO add your handling code here:
//        DienThoai dt1 = new DienThoai();
        String ma = txtMa.getText();
        String hang = cboHang.getSelectedItem().toString();
        String mauSac = "";
        if (rdDen.isSelected()) {
            mauSac = "Đen";
        } else if (rdTrang.isSelected()) {
            mauSac = "Trắng";
        } else {
            mauSac = "Vàng";
        }
        String giaDT = txtGia.getText().trim();

        String boNho = txtBoNho.getText();

        if (ma.trim().isEmpty() || hang.trim().isEmpty() || mauSac.trim().isEmpty() || giaDT.isEmpty() || boNho.trim().isEmpty()) {

            JOptionPane.showMessageDialog(this, "Ko đc để trống");
        } else {
            Integer gia = Integer.valueOf(giaDT);

            if (gia < 100) {
                JOptionPane.showMessageDialog(this, "Phải là số nguyên dương và lớn hơn 100");
            } else {
                DienThoai dt = new DienThoai(ma, hang, mauSac, boNho, gia);
                Boolean check = qldt.add(dt);
                if (check) {
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                    loadData(qldt.getListDienThoai());
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm thất bại");
                }
            }
        }


    }//GEN-LAST:event_btnThemMouseClicked

