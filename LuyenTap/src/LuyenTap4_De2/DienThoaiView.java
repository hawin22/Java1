    void loadData(ArrayList<DienThoai> list) {
        int STT = 1;
        defaultTableModel = (DefaultTableModel) tblDienThoai.getModel();
        defaultTableModel.setRowCount(0);
        for (DienThoai dienThoai : list) {
            defaultTableModel.addRow(new Object[]{
                STT++,
                dienThoai.getMa(),
                dienThoai.getHang(),
                dienThoai.getMauSac(),
                dienThoai.getGia(),
                dienThoai.getBoNho()
            });
        }
    }

    public void cuoi() {
        int i = tblDienThoai.getRowCount() - 1;
        String ma = (String) tblDienThoai.getValueAt(i, 1);
        txtMa.setText(ma);
        String hang = (String) tblDienThoai.getValueAt(i, 2);
        cboHang.setSelectedItem(hang);
        String mauSac = (String) tblDienThoai.getValueAt(i, 3);
        if (mauSac.equals("Đen")) {
            rdDen.setSelected(true);
        } else if (mauSac.equals("Trắng")) {
            rdTrang.setSelected(true);
        } else {
            rdVang.setSelected(true);
        }
        Integer gia = (Integer) tblDienThoai.getValueAt(i, 4);
        String giaDT = String.valueOf(gia);
        txtGia.setText(giaDT);
        String boNho = (String) tblDienThoai.getValueAt(i, 5);
        txtBoNho.setText(boNho);
    }

