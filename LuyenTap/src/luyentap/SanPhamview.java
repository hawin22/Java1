    private void tblSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanPhamMouseClicked
        // TODO add your handling code here:
        int i = tblSanPham.getSelectedRow();
        String ma = (String) tblSanPham.getValueAt(i, 0);
        txtMa.setText(ma);
        String ten = (String) tblSanPham.getValueAt(i, 1);
        txtTen.setText(ten);

        Integer soLuong = (Integer) tblSanPham.getValueAt(i, 2);
        String sL = String.valueOf(soLuong);
        txtSoLuong.setText(sL);

        Integer giaBan = (Integer) tblSanPham.getValueAt(i, 3);
        String gia = String.valueOf(giaBan);
        txtDonGia.setText(gia);

        Integer tien = (Integer) tblSanPham.getValueAt(i, 4);
        String thanhTien = String.valueOf(tien);
        txtThanhTien.setText(thanhTien);

        String danhMuc = (String) tblSanPham.getValueAt(i, 5);
        cboDanhMuc.setSelectedItem(danhMuc);

        String trangThai = (String) tblSanPham.getValueAt(i, 6);
        if (trangThai.equals("Còn hàng")) {
            rbCon.setSelected(true);
        } else {
            rbHer.setSelected(true);
        }


    }//GEN-LAST:event_tblSanPhamMouseClicked

    private void btnThemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThemMouseClicked
        // TODO add your handling code here:
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        Integer soLuong = Integer.parseInt(txtSoLuong.getText());
        Integer donGia = Integer.parseInt(txtDonGia.getText());
        Integer tien = soLuong * donGia;
        String danhMuc = cboDanhMuc.getSelectedItem().toString();
        String trangThai = "";
        if (rbCon.isSelected()) {
            trangThai = "Còn hàng";
        } else {
            trangThai = "Hết hàng";
        }
        SanPham sanPham = new SanPham(ma, ten, soLuong, donGia, tien, danhMuc, trangThai);

        Boolean check = qlsp.add(sanPham);
        if (check) {
            JOptionPane.showMessageDialog(this, "Thêm thành công");
            loadData(qlsp.getListSanPham());
        } else {
            JOptionPane.showMessageDialog(this, "Ko đc trùng mã");
        }
    }//GEN-LAST:event_btnThemMouseClicked

    private void btnXoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnXoaMouseClicked
        // TODO add your handling code here:
        int i = tblSanPham.getSelectedRow();
        Boolean check = qlsp.remove(i);
        if (check) {
            JOptionPane.showMessageDialog(this, "Đã xóa thành công");
            loadData(qlsp.getListSanPham());
        }
    }//GEN-LAST:event_btnXoaMouseClicked

