    void loadData(ArrayList<SanPham> list) {
        int thanhTien = 0;
        defaultTableModel = (DefaultTableModel) tblSanPham.getModel();
        defaultTableModel.setRowCount(0);
        for (SanPham sanPham : list) {
            defaultTableModel.addRow(new Object[]{
                sanPham.getMaSP(),
                sanPham.getTenSP(),
                sanPham.getSoLuong(),
                sanPham.getGiaBan(),
                thanhTien = sanPham.getSoLuong() * sanPham.getGiaBan(),
                sanPham.getMaDanhMuc(),
                sanPham.getTrangThai()

            });
        }

    }

