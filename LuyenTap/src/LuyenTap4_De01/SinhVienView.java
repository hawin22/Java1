    private void tblSinhVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSinhVienMouseClicked
        // TODO add your handling code here:
        int i = tblSinhVien.getSelectedRow();
        String ma = (String) tblSinhVien.getValueAt(i, 1);
        txtMa.setText(ma);
        String ten = (String) tblSinhVien.getValueAt(i, 2);
        txtTen.setText(ten);
        String gioiTinh = (String) tblSinhVien.getValueAt(i, 3);
        if (gioiTinh.equals("Nam")) {
            rdNam.setSelected(true);
        } else {
            rdNu.setSelected(true);
        }
        Integer namSinh = (Integer) tblSinhVien.getValueAt(i, 4);
        String nam = String.valueOf(namSinh);
        cboNamSinh.setSelectedItem(nam);
    }//GEN-LAST:event_tblSinhVienMouseClicked

    private void btnXoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnXoaMouseClicked
        // TODO add your handling code here:
        int checks = JOptionPane.showConfirmDialog(this, "B có muốn xóa ko ???");
        if (checks != JOptionPane.YES_OPTION) {
            return;
        } else {
            int i = tblSinhVien.getSelectedRow();
            Boolean check = qlsv.delete(i);
            if (check) {
                JOptionPane.showMessageDialog(this, "Xóa thành công");
                loadData(qlsv.listSV);
            } else {
                JOptionPane.showMessageDialog(this, "Xóa thất bại");
            }
        }
    }//GEN-LAST:event_btnXoaMouseClicked

