    private void btnGhiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGhiActionPerformed
        // TODO add your handling code here:
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        String gioiTinh = "";
        if (rdNam.isSelected()) {
            gioiTinh = "Nam";
        } else {
            gioiTinh = "Nữ";
        }
        Integer namSinh = Integer.parseInt(cboNamSinh.getSelectedItem().toString());
        SinhVien sv = new SinhVien(ma, ten, gioiTinh, namSinh);
        int checks = JOptionPane.showConfirmDialog(this, "B có muốn thêm ko???");
        if (checks != JOptionPane.YES_OPTION) {
            return;
        }else{
        if (sv.getMaSV().trim().isEmpty() || sv.getTenSV().trim().isEmpty() || sv.getGioiTinh().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống");
        } else {
            Boolean check = qlsv.add(sv);
            if (check) {
                JOptionPane.showMessageDialog(this, "Thêm thành công");
                loadData(qlsv.getListSinhVien());
            } else {
                JOptionPane.showMessageDialog(this, "Thêm thất bại");
            }
            JOptionPane.showMessageDialog(this, qlsv.ghiFile(fileName));
        }}
    }//GEN-LAST:event_btnGhiActionPerformed


