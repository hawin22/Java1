public class Bai3 {

    private String ten;
    private double gia;

    public Bai3() {
        this.ten = "";
        this.gia = 0;
    }

    public Bai3(String ten, double gia) {
        this.ten = ten;
        this.gia = gia;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public double getGia() {
        return gia;
    }

    public void setGia(double gia) {
        this.gia = gia;
    }

}
